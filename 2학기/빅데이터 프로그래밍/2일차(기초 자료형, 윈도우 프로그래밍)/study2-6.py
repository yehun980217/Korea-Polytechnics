# 튜플(tuple) = 리스트와 거의 비슷
t1 = ()
t3 = (1,2,3)
t4 = 1,2,3 # 괄호 생략 가능
t5 = ('a','b',('ab','cd'))

# 인덱싱
t1 = (1, 2, 'a', 'b')
print(t1[0]) # 1
print(t1[3]) # b

# 슬라이싱
t1 = (1,2,'a','b')
print(t1[1:]) # (2, 'a', 'b')

# 튜플 더하기
t2 = (3, 4)
print(t1 + t2) # (1, 2, 'a', 'b', 3, 4)

# 튜플 곱하기
print(t2 * 3) # (3, 4, 3, 4, 3, 4)